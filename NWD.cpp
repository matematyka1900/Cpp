#include <iostream>

using namespace std;



int main()
{
	float a, b, c;
	float delta;
	int n;
	
	for (int i = 0; i < n; i++)
	{
		

		delta = ((b*b) - 4 * a * c);
		if (delta > 0)
		{
			cout << 2 << endl;
		}
		else if (delta == 0)
		{
			cout << 1 << endl;

		}
		else
		{
			cout << 0 << endl;
		}

		
	}
	
}